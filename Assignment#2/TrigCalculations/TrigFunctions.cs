using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Assignment_2.TrigCalculations
{
    internal class TrigFunctions
    {
        public static double CalculateSin2Theta(double theta)
        {
            return Math.Sin(2 * theta);
        }

        public static double CalculateCos3Theta(double theta)
        {
            return Math.Cos(3 * theta);
        }

        public static double CalculateTanTheta(double theta)
        {
            return Math.Tan(theta);
        }
    }
}
